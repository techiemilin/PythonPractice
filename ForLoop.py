# range() function is used in for loop

# Example 1 (Print 1 to 10 Numbers) 
for i in range (10):
    print(i)
print("*****************")
    
# Example 2  (1 is starting index and 11 is ending/maximum (range) index)
for i in range (1, 11):
    print(i)
print("*****************")
# Example 3  (0 is starting range, 10 is ending/ maximum range and 2 is incremental value to print the even numbers)
for i in range (0, 10, 2):
    print(i)
print("*****************")
    
# Example 4 
for i in range(1, 12, 2):
    print(i)
print("*****************")

# Example 5 
for i in range (10, -10, -1):
    print(i)
