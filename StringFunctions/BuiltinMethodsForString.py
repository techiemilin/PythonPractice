a = "Milin Patel"

print(a.isalnum())  # False no alphanumeric

print("Milin".isalpha())  # true as in has alphabet 

print(a.isdigit())  # is false it is not digits or number

print("20".isdigit())  # true as it is a number

print("First number".isidentifier())  # false as it is number

print(a.islower())  # false because string is not all in lower case

print("milin".islower())  # true is in lower cases

print("MILIN".isupper())  # true as everything in upper case

print("milin".isupper())  # false as its not in upper case

print(" ".isspace())  # true as it is space
