a = "milin"

print(a.capitalize()) # return copy of string 1st letter capital

print(a.lower()) # covert every character into lower case

print(a.upper()) # covert upper case

print(a.title()) # 1st char. capital

print(a.swapcase()) # return from lower cases into upper cases

print(a.replace("milin", "new", 1)) # replace the new string in place of old string